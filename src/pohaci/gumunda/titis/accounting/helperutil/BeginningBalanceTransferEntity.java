public class BeginningBalanceTransferEntity {
	private String code;
	private String desc;
	private String curr;
	private Double debit;
	private Double credit;
	private Double debitrp;
	private Double creditrp;
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code The code to set.
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return Returns the credit.
	 */
	public Double getCredit() {
		return credit;
	}
	/**
	 * @param credit The credit to set.
	 */
	public void setCredit(Double credit) {
		this.credit = credit;
	}
	/**
	 * @return Returns the creditrp.
	 */
	public Double getCreditrp() {
		return creditrp;
	}
	/**
	 * @param creditrp The creditrp to set.
	 */
	public void setCreditrp(Double creditrp) {
		this.creditrp = creditrp;
	}
	/**
	 * @return Returns the curr.
	 */
	public String getCurr() {
		return curr;
	}
	/**
	 * @param curr The curr to set.
	 */
	public void setCurr(String curr) {
		this.curr = curr;
	}
	/**
	 * @return Returns the debit.
	 */
	public Double getDebit() {
		return debit;
	}
	/**
	 * @param debit The debit to set.
	 */
	public void setDebit(Double debit) {
		this.debit = debit;
	}
	/**
	 * @return Returns the debitrp.
	 */
	public Double getDebitrp() {
		return debitrp;
	}
	/**
	 * @param debitrp The debitrp to set.
	 */
	public void setDebitrp(Double debitrp) {
		this.debitrp = debitrp;
	}
	/**
	 * @return Returns the desc.
	 */
	public String getDesc() {
		return desc;
	}
	/**
	 * @param desc The desc to set.
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
}
